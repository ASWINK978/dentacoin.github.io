function minus1() {
  var plus = document.getElementById("plus1").innerHTML;
  if (plus == '+') {
    document.getElementById("plus1").innerHTML = "-";
  }
  else if(plus == '-') {
    document.getElementById("plus1").innerHTML = "+";
  }
}
